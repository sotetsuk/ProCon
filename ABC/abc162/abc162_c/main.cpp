ll gcd(ll a, ll b) {
    while(true) {
        if(a < b) swap(a, b);
        if(!b) break;
        a %= b;
    }
    return a;
}

ll gcd(ll a, ll b, ll c) {
    ll x = gcd(a, b);
    ll y = gcd(b, c);
    return gcd(x, y);
}

